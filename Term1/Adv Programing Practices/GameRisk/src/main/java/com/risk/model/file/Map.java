package com.risk.model.file;

/**
 * This is an Model Class for Map.
 * 
 * @author <a href="mailto:mayankjariwala1994@gmail.com">MayankJariwala</a>
 * @version 0.0.1
 */
public class Map {

	private String author;
	private String image;
	private String wrap;
	private String scroll;
	private String warn;

	/**
	 * @return the author
	 */
	public String getAuthor() {
		return author;
	}

	/**
	 * @param author : the author to set
	 */
	public void setAuthor(String author) {
		this.author = author;
	}

	/**
	 * @return the image
	 */
	public String getImage() {
		return image;
	}

	/**
	 * @param image : the image to set
	 */
	public void setImage(String image) {
		this.image = image;
	}

	/**
	 * @return the wrap
	 */
	public String getWrap() {
		return wrap;
	}

	/**
	 * @param wrap : the wrap to set
	 */
	public void setWrap(String wrap) {
		this.wrap = wrap;
	}

	/**
	 * @return the scroll
	 */
	public String getScroll() {
		return scroll;
	}

	/**
	 * @param scroll : the scroll to set
	 */
	public void setScroll(String scroll) {
		this.scroll = scroll;
	}

	/**
	 * @return the warn
	 */
	public String getWarn() {
		return warn;
	}

	/**
	 * @param warn : the warn to set
	 */
	public void setWarn(String warn) {
		this.warn = warn;
	}

}
